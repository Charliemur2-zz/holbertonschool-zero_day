/**
 * op_add - add tow numbers
 * @a: first number
 * @b: second number
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subs tow numbers
 * @a: first number
 * @b: second number
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mult tow numbers
 * @a: first number
 * @b: second number
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide tow numbers
 * @a: first number
 * @b: second number
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod tow numbers
 * @a: first number
 * @b: second number
 * Return: a + b
 */
int mod(int a, int b)
{
	return (a % b);
}
